numbers = []        #1. 리스트를 선언합니다.

for i in range(6):  #2. 0~5 사이의 숫자를 i변수에 대입합니다.
    numbers.append(i) #3.리스트로 정의된 변수 number에 i의 요소값(for range를 통해 생선된 값들)들을 차례대로 속성을 추가합니다.
    print(numbers) #4. 위의 작업을 통해 순서대로 추가되고 있는 값들을 차례대로 출력합니다.\ <--

print(numbers) # 해당값이 다른이유는 위의 순서가 모두 끝난 뒤의 값을  출력하기 때문이다.(위의 과정을 탈출 한것으로 보자!!)


